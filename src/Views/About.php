    <h2>Nuestra misión</h2>
    <p>En What2Cook creemos que una mejor alimentación debería ser más accesible, clara y fácil de gestionar. Nuestra misión es ayudar a las personas a tomar decisiones alimentarias informadas mediante herramientas que combinen cocina, nutrición y tecnología en una sola experiencia.
    <br>
    Buscamos simplificar el proceso de cocinar y planificar comidas, ofreciendo recomendaciones basadas en ingredientes disponibles, preferencias dietarias y objetivos nutricionales. Queremos que cada usuario pueda ahorrar tiempo, reducir desperdicios y encontrar opciones que se adapten a su estilo de vida.
    <br>
    Más allá de las recetas, aspiramos a construir una plataforma que promueva una relación más consciente con la alimentación, brindando información confiable, personalización y recursos que acompañen distintos hábitos y necesidades alimentarias.</p>
