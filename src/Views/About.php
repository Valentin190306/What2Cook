    <h2>Nuestra historia</h2>
    <p>What2Cook nació a partir de una pregunta simple: ¿qué puedo cocinar con lo que ya tengo? A partir de esa idea, comenzamos a imaginar una plataforma que no solo ayudara a encontrar recetas, sino que también facilitara la planificación de comidas, la organización nutricional y el descubrimiento de distintos estilos de alimentación.
    <br>
    Durante el desarrollo del proyecto identificamos un problema común: muchas personas quieren comer mejor, cocinar más o seguir una dieta específica, pero se encuentran con herramientas fragmentadas, información dispersa o procesos poco prácticos. Por eso decidimos crear una aplicación que reúna en un mismo lugar recetas, asistentes inteligentes, información nutricional y planificación alimentaria.
    <br>
    Nuestro objetivo fue diseñar una experiencia simple, útil y adaptable a distintas necesidades, desde alguien que busca una receta rápida con ingredientes disponibles hasta quien necesita organizar un plan alimenticio completo.</p>
